from abc import ABC, abstractmethod
from typing import Dict, Any

class BaseMetric(ABC):
    """Abstract base class for a single metric calculation."""
    @abstractmethod
    def calculate(self, llm_response: str, **kwargs) -> Dict[str, Any]:
        """
        Calculates a specific metric or set of related metrics.
        Args:
            llm_response: The response generated by the LLM.
            **kwargs: Additional arguments specific to the metric.
        Returns:
            A dictionary of calculated metrics.
        """
        pass
